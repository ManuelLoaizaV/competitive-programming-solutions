Double f(Long h, Long c, Long n, Long t) {
	Double tn = (((n + 1) / 2 * h + n / 2 * c) / (Double) n);
	return fabs(tn - t);
}

void solve() {
    Long h, c, t;
	cin >> h >> c >> t;
	Long den = 2 * t - h - c;
	if (den <= 0) {
		cout << 2 << endl;
	} else {
		Long k = (h - t) / den;
		Long pos = 2 * k + 1;
		vector<pair<Double, Long>> ans;
		ans.push_back({f(h, c, pos, t), pos});
		ans.push_back({f(h, c, pos + 2, t), pos + 2});
		ans.push_back({f(h, c, 2, t), 2});
		sort(all(ans));
		cout << ans[0].second << endl;
	}
}

